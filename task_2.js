function findDifferentNumber(a, b, c) {
    if (a == b && b == c) {
      // якщо всі три числа рівні, повертаємо -1
      return -1;
    } else if (a == b) {
      // якщо перше і друге число однакові, повертаємо третє
      return c;
    } else if (a == c) {
      // якщо перше і третє число однакові, повертаємо друге
      return b;
    } else {
      // якщо друге і третє число однакові, повертаємо перше
      return a;
    }
  }
  